void port_scan_services(t_scan* const result) {

    t_port_state* ptr = NULL;
    for(uint16_t x = 0; result->ports[x].port; x++) {

        ptr = &result->ports[x];

        if(ptr->udp_scan_state == PORT_OPEN) 
            ptr->service = get_udp_service(ptr->port);

        else ptr->service = get_tcp_service(ptr->port);
    }
}
